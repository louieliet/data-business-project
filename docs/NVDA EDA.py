"""## 4. Limpieza ligera y feature engineering

"""

df["Date"] = pd.to_datetime(df["Date"], errors="coerce")

price_columns = ["Open", "High", "Low", "Close", "Adj Close"]
numeric_columns = price_columns + ["Volume"]

for column in numeric_columns:
    df[column] = pd.to_numeric(df[column], errors="coerce")

df = df.sort_values("Date").reset_index(drop=True)

df["Year"] = df["Date"].dt.year
df["Month"] = df["Date"].dt.month
df["Month Name"] = df["Date"].dt.month_name()
df["Quarter"] = df["Date"].dt.quarter
df["Day of Week"] = df["Date"].dt.day_name()

df["Daily Return"] = df["Adj Close"].pct_change()
df["Log Return"] = np.log(df["Adj Close"] / df["Adj Close"].shift(1))
df["Intraday Return"] = (df["Close"] - df["Open"]) / df["Open"]
df["High Low Spread"] = df["High"] - df["Low"]
df["High Low Spread Pct"] = df["High Low Spread"] / df["Open"]
df["Close Open Spread"] = df["Close"] - df["Open"]

df["MA 20"] = df["Adj Close"].rolling(window=20).mean()
df["MA 50"] = df["Adj Close"].rolling(window=50).mean()
df["MA 200"] = df["Adj Close"].rolling(window=200).mean()
df["Rolling Volatility 20"] = df["Daily Return"].rolling(window=20).std() * np.sqrt(252)
df["Rolling Volatility 60"] = df["Daily Return"].rolling(window=60).std() * np.sqrt(252)

df["Cumulative Return"] = (1 + df["Daily Return"].fillna(0)).cumprod() - 1
df["Cumulative Max"] = df["Adj Close"].cummax()
df["Drawdown"] = df["Adj Close"] / df["Cumulative Max"] - 1

