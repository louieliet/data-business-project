"""## 14. Detección programática de outliers

"""

def iqr_outlier_summary(data: pd.DataFrame, columns: list[str]) -> pd.DataFrame:
    rows = []

    for column in columns:
        clean_series = data[column].dropna()
        q1 = clean_series.quantile(0.25)
        q3 = clean_series.quantile(0.75)
        iqr = q3 - q1
        lower_bound = q1 - 1.5 * iqr
        upper_bound = q3 + 1.5 * iqr
        outlier_mask = data[column].lt(lower_bound) | data[column].gt(upper_bound)

        rows.append({
            "column": column,
            "q1": q1,
            "q3": q3,
            "iqr": iqr,
            "lower_bound": lower_bound,
            "upper_bound": upper_bound,
            "outlier_count": int(outlier_mask.sum()),
            "outlier_pct": round(outlier_mask.mean() * 100, 2),
        })

    return pd.DataFrame(rows).sort_values("outlier_count", ascending=False)

outlier_columns = ["Daily Return", "Log Return", "Intraday Return", "High Low Spread Pct", "Volume", "Rolling Volatility 20"]
outlier_summary = iqr_outlier_summary(df, outlier_columns)

